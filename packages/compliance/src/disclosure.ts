export type PricingTier = "free" | "starter" | "growth" | "scale" | "agency";
export type Locale = "en-US" | "en-GB" | "es-ES" | "es-MX" | "pt-BR" | "fr-FR" | "de-DE";

export interface DisclosureOptions {
  tier: PricingTier;
  /** Operator name shown in the disclosure, e.g. workspace owner / business name. */
  operatorName: string;
  /** Customizable label override (paid tiers only). */
  customLabel?: string;
  /** Where it's being rendered — affects format. */
  surface: "page_footer" | "voice_preamble" | "sms_first_message" | "email_footer" | "ad_creative";
  /** Locale for the disclosure text. */
  locale?: Locale;
  /** Set to false to attempt to suppress — only honored on paid tiers. */
  visible?: boolean;
}

export interface DisclosureRendered {
  /** True if the disclosure was rendered (free tier never suppresses). */
  visible: boolean;
  /** Final text. */
  text: string;
  /** HTML representation (for page_footer / email_footer). */
  html: string;
  /** Hash of the canonical text (for audit). */
  textHash: string;
  /** Surface used. */
  surface: DisclosureOptions["surface"];
  /** Locale used. */
  locale: Locale;
}

