function pickTemplate(locale: Locale, surface: DisclosureOptions["surface"]): string {
  const set = TEMPLATES[locale] ?? TEMPLATES["en-US"];
  switch (surface) {
    case "page_footer":
      return set.page;
    case "voice_preamble":
      return set.voice;
    case "sms_first_message":
      return set.sms;
    case "email_footer":
      return set.email;
    case "ad_creative":
      return set.ad;
  }
}

function interpolate(template: string, vars: Record<string, string>): string {
  return template.replace(/\{(\w+)\}/g, (_, k: string) => vars[k] ?? `{${k}}`);
}

function sha256(input: string): string {
  // Tiny inline sha256 import — kept local so this file is browser-safe in
  // the Next.js render path if needed.
  // eslint-disable-next-line @typescript-eslint/no-var-requires, @typescript-eslint/no-require-imports
  const { createHash } = require("node:crypto") as typeof import("node:crypto");
  return createHash("sha256").update(input).digest("hex");
}

function escapeHtml(s: string): string {
  return s.replace(/[&<>"']/g, (c) =>
    ({ "&": "&amp;", "<": "&lt;", ">": "&gt;", '"': "&quot;", "'": "&#39;" })[c] ?? c,
  );
}

export function renderDisclosure(opts: DisclosureOptions): DisclosureRendered {
  const locale = opts.locale ?? "en-US";
  const isFree = opts.tier === "free";
  const wantsVisible = opts.visible ?? true;
  // Free tier: ALWAYS visible. Paid tiers: customer may suppress with visible:false.
  const visible = isFree ? true : wantsVisible;

  const tpl = opts.customLabel && !isFree ? opts.customLabel : pickTemplate(locale, opts.surface);
  const text = interpolate(tpl, { operator: opts.operatorName, agent: "{agent}" });
  const html = `<div class="ai-disclosure" data-tier="${opts.tier}" data-surface="${opts.surface}">${escapeHtml(text)}</div>`;

  return {
    visible,
    text,
    html,
    textHash: sha256(text),
    surface: opts.surface,
    locale,
  };
}

/**
 * Resolve the operator string shown in the disclosure.
 * Falls back to "the operator" if no name provided.
 */
export function operatorName(input: { businessName?: string | null; userFullName?: string | null }): string {
  if (input.businessName && input.businessName.trim()) return input.businessName.trim();
  if (input.userFullName && input.userFullName.trim()) return input.userFullName.trim();
  return "the operator";
}
